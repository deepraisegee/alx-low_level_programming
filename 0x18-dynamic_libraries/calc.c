#include "main.h"

/**
  * add - add 2 numbers
  * @a: 1st No
  * @b: 2nd No
  *
  * Return: the result
  */
int add(int a, int b)
{
	return (a + b);
}

/**
  * sub - subtract 2 numbers
  * @a: 1st No
  * @b: 2nd No
  *
  * Return: the result
  */
int sub(int a, int b)
{
	return (a - b);
}

/**
  * mul - multiply 2 numbers
  * @a: 1st No
  * @b: 2nd No
  *
  * Return: the result
  */
int mul(int a, int b)
{
	return (a * b);
}

/**
  * div - divide 2 numbers
  * @a: 1st No
  * @b: 2nd No
  *
  * Return: the result
  */
int div(int a, int b)
{
	return (a / b);
}

/**
  * mod - mod of 2 numbers
  * @a: 1st No
  * @b: 2nd No
  *
  * Return: the result
  */
int mod(int a, int b)
{
	return (a % b);
}
